number_1 = float(input("enter please the first number: "))
number_2 = float(input("enter please the second number: "))
op = (input("enter please the op: "))

if op == "+":
    print(number_1 + number_2)
elif op == "-":
    print(number_1 - number_2)
elif op == "/":
    print(number_1 / number_2)
elif op == "*":
    print(number_1 * number_2)